/*
    Demonsta sequências de escape em strings.
*/

public class StrDemo {
    public static void main(String[] args) {
        System.out.println("First line\nSecond line"); // sequência de escape '\n' gera uma nova linha
        System.out.println("A\tB\tC"); // sequência de escape '\t' cria uma tabulação horizontal
        System.out.println("D\tE\tF");
    }
}